	onepageSupport: (!jQuery.browser.msie || jQuery.browser.version >= 10) ? true : false,
	currentIndex: 0,
	total: jQuery('#topics .topic').length,
	animatedTopics: {},
};

jQuery(function($) {	
	$('#topic-home .sliders').slick({
		dots: false,
		infinite: true,
		slidesToScroll: 1,
		autoplay: true,
		autoplaySpeed: 3000,
		arrows: false,
		speed: 3000,
		variableWidth: true,
		pauseOnHover: false,
		useCss: false,	
		fade: true	
	});

	// 动画
	$('.propaganda .line-1').fadeIn(1000);
	setTimeout(function() {
		$('.propaganda .line-2').fadeIn(1000);
	}, 1200);
	
	// IE 10以下不支持 OnePage Scroll
	if(topicAnimation.onepageSupport) {
		$('html').css('overflow-y', 'hidden');
		$("#topics").onepage_scroll({
			sectionContainer: ".topic",     // sectionContainer accepts any kind of selector in case you don't want to use section
			easing: "ease",                  // Easing options accepts the CSS3 easing animation such "ease", "linear", "ease-in",
			                                    // "ease-out", "ease-in-out", or even cubic bezier value such as "cubic-bezier(0.175, 0.885, 0.420, 1.310)"
			animationTime: 500,             // AnimationTime let you define how long each section takes to animate
			pagination: false,                // You can either show or hide the pagination. Toggle true for show, false for hide.
			updateURL: false,                // Toggle this true if you want the URL to be updated automatically when the user scroll to each page.
			beforeMove: function(index) {},  // This option accepts a callback function. The function will be called before the page moves.
			afterMove: function(index) {},   // This option accepts a callback function. The function will be called after the page moves.
			loop: true,                     // You can have the page loop back to the top/bottom when the user navigates at up/down on the first/last page.
			keyboard: true,                  // You can activate the keyboard controls
			responsiveFallback: false,        // You can fallback to normal page scroll by defining the width of the browser in which
			                                    // you want the responsive fallback to be triggered. For example, set this to 600 and whenever
			                                    // the browser's width is less than 600, the fallback will kick in.
			direction: "vertical",            // You can now define the direction of the One Page Scroll animation. Options available are "vertical" and "horizontal". The default value is "vertical".  
			afterMove: function(index) {
				// 合作伙伴动画
				if(index == 6) {
					$('#topic-partners .comment-1 img').fadeIn(500);
					setTimeout(function() {
						$('#topic-partners .comment-2 img').fadeIn(500);						
					}, 1600);
					
					setTimeout(function() {
						$('#topic-partners .comment-3 img').fadeIn(500);
					}, 800);
										
				}
			}
		});
	}	

	// 手动翻页
	$('#arrow-down > img').click(function() {
		if(topicAnimation.onepageSupport) {
			$("#topics").moveDown();
		}
		else {			
			topicAnimation.currentIndex ++;
			if(topicAnimation.currentIndex == topicAnimation.total) {
				topicAnimation.currentIndex = 0;
				$.scrollTo(0, 500);
			}
			else {				
				$.scrollTo($($('#topics .topic')[topicAnimation.currentIndex]).offset().top, 500);				
			}
		}
	});

	// 体验馆切换
	$('#topic-stadium .locator').click(function() {	
		var locator = this;
		$('.locator.active').removeClass('active');
		$(this).addClass('active');	
		$('#topic-stadium .introduction.active').fadeOut(300, function() {
			$(this).removeClass('active');
			$('#topic-stadium .introduction.' + $(locator).data('tab')).addClass('active');
			$('#topic-stadium .introduction.' + $(locator).data('tab')).fadeIn(300);
		});
		//$('#topic-stadium .introduction.active').removeClass('active');		
	});

