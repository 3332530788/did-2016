				<p class="top">1988年，德国纪录片记者安德烈•汉尼克（Andreas Heineck)先生给自己换了新份工作，过去几年中一位双目失明的同事让他对视障者的世界产生了浓厚的兴趣。几经推敲，他在德国法兰克福建立了世界上第一家黑暗中对话体验馆。黑暗中对话自在德国设立迄今，已经实施遍及世界40个国家，170 余个城市，超过1000万人以上的参与见证。</p>
				<p>2011年3月，获得了德国“对话社会企业”（DSE）大陆地区唯一授权后，黑暗中对话在上海正式成立。从上海市社会创新孵化园的一间小办公室起步，陆续建立了上海、成都、北京和深圳四个分支机构，在十多个城市为企业、政府等机构提供工作坊、体验馆等活动。</p>
				<p class="bottom" style="margin-bottom:40px">多年来，对话社会企业，简称DSE( Dialogue Social Enterprise ) 不断开发新的对话服务项目，从黑暗中对话（Dialogue in the Dark）延伸到无声中对话（Dialogue in Silence）以及对话时间（Dialogue with Time）。作为对话社会企业最重要也是最具影响力的品牌——黑暗中对话目前已经开发出包括黑暗中对话体验馆和黑暗中对话工作坊、暗中咖啡、暗中音乐会、暗中生日会、暗中派对等基于“黑暗”平台的丰富的产品，让大众来领略“黑暗”的力量。</p>
